CHECKMATE = 1000
STALEMATE = 0
DEPTH = 3


def evaluateBoard(game_state):
    """
    A simple evaluation function that considers material score only.
    """
    if game_state.checkmate:
        score = CHECKMATE
        if game_state.white_to_move:
            return -CHECKMATE  # black wins
        else:
            return CHECKMATE  # white wins
    elif game_state.stalemate:
        score = STALEMATE 
        return STALEMATE
    score = 0
    for row in range(len(game_state.board)):
        for col in range(len(game_state.board[row])):
            piece = game_state.board[row][col]
            if piece != "--":
                piece_position_score = 0
                if piece[1] != "K":
                    piece_position_score = piece_position_scores[piece][row][col]
                if piece[0] == "w":
                    score += piece_score[piece[1]] + piece_position_score
                if piece[0] == "b":
                    score -= piece_score[piece[1]] + piece_position_score

    return score

def quiescenceSearch(game_state, alpha, beta, maximizing_player, DEPTH):
    stand_pat = evaluateBoard(game_state)

    if DEPTH == 0 or game_state.checkmate or game_state.stalemate:
        return stand_pat

    if maximizing_player:
        max_eval = -CHECKMATE
        for move in game_state.getValidMoves(): 
            game_state.makeMove(move)
            eval = -quiescenceSearch(game_state, -beta, -alpha, not maximizing_player, DEPTH - 1)
            game_state.undoMove()

            max_eval = max(max_eval, eval)
            alpha = max(alpha, eval)

            if beta <= alpha:
                break

        return max_eval
    else:
        min_eval = CHECKMATE
        for move in game_state.getValidMoves(): 
            game_state.makeMove(move)
            eval = -quiescenceSearch(game_state, -beta, -alpha, not maximizing_player , DEPTH - 1)
            game_state.undoMove()

            min_eval = min(min_eval, eval)
            beta = min(beta, eval)


            if beta <= alpha:
                break

        return min_eval

def findBestMoveQuiescenceSearch(game_state, valid_moves, return_queue):
    global next_move
    next_move = None
    maximizing_player = game_state.white_to_move
    random.shuffle(valid_moves)
    best_score = -CHECKMATE
    for move in valid_moves:
        game_state.makeMove(move)
        score = -quiescenceSearch(game_state, -CHECKMATE, CHECKMATE, maximizing_player, DEPTH)
        game_state.undoMove()
        if score > best_score:
            best_score = score
            next_move = move
    return_queue.put(next_move)



